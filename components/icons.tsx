// SVG Icon Components for Liquid Glass Design System
// All icons scale proportionally with size and support weight variants

interface IconProps {
  className?: string
  size?: number
  weight?: 'light' | 'regular' | 'bold'
  withShadow?: boolean
}

// Calculate stroke width based on size and weight for optical consistency
const getStrokeWidth = (size: number, weight: 'light' | 'regular' | 'bold' = 'regular') => {
  const baseStroke = size / 12  // 2px at 24px size
  const weightMultipliers = {
    light: 0.75,      // 1.5px at 24px
    regular: 1,       // 2px at 24px
    bold: 1.4         // 2.8px at 24px
  }
  return baseStroke * weightMultipliers[weight]
}

// Get drop shadow filter for glass surfaces
const getShadowStyle = (withShadow: boolean) =>
  withShadow ? { filter: 'drop-shadow(0 2px 8px rgba(0, 0, 0, 0.3))' } : undefined

export const CheckIcon = ({ className = "", size = 24, weight = 'regular', withShadow = false }: IconProps) => {
  const strokeWidth = getStrokeWidth(size, weight)
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      style={getShadowStyle(withShadow)}
    >
      <polyline points="20 6 9 17 4 12" />
    </svg>
  )
}

